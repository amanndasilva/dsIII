num1 = float(input("Insira o primeiro numero: "))
num2 = float(input("Insira o segundo numero: "))

soma = num1 + num2
soma1 = round(soma, 2)

sub = num1 - num2
sub1 = round(sub, 2)

multi = num1 * num2
multi1 = round(multi, 2)

div = num1 / num2
div1 = round(div, 2)

print("O resultado das operacoes com eses numeros e:", soma1, "|", sub1, "|", multi1, "|", div1)